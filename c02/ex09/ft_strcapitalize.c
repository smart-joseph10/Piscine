char	*ft_strcapitalize(char *str)
{
	int	i;

	i = 1;
	if ('a' <= str[0] && str[0] <= 'z')
	{
		str[0] -= 32;
	}
	while (str[i])
	{
		if ((1 <= str[i - 1] && str[i - 1] <= 47) ||
			(58 <= str[i - 1] && str[i - 1] <= 64) ||
			(91 <= str[i - 1] && str[i - 1] <= 96) ||
			(123 <= str[i - 1] && str[i - 1] <= 127))
		{
			if (97 <= str[i] && str[i] <= 122)
				str[i] -= 32;
		}
		else if ('A' <= str[i] && str[i] <= 'Z')
			str[i] += 32;
		i++;
	}
	return (str);
}
