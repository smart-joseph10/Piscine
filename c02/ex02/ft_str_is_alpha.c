int		ft_str_is_alpha(char *str)
{
	int	j;

	j = 0;
	while (str[j])
	{
		if (('a' <= str[j] && str[j] <= 'z') ||
				('A' <= str[j] && str[j] <= 'Z'))
			j++;
		else
			return (0);
	}
	return (1);
}
